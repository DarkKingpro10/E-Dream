    public void ColocarImagen(){
        try{
            ControladorVariables variables = new ControladorVariables();
            Blob imagenEmpleado = variables.getImagen();
            
            //Se carga la cadena de byte
            byte[] imagene = (byte[]) imagenEmpleado.getBytes(1, (int)imagenEmpleado.length());
            //Se leen los datos
            BufferedImage img = ImageIO.read(new ByteArrayInputStream(imagene));
            //Se carga la imagen en los formularios
            Image imagen = img.getScaledInstance(lblFoto.getWidth(), lblFoto.getHeight(), Image.SCALE_SMOOTH);
            lblFoto.setIcon(new ImageIcon(imagen));
            lblFoto.setIcon(new ImageIcon(imagen));
        }catch (Exception e){
            //Se envía un error si no es compatible
        }
    }
    
