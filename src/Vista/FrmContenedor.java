    private void btnReporteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnReporteMouseClicked
        PanelBlanco.setVisible(false);
        FrmReportes Reportes = new FrmReportes();
        if (this.MAXIMIZED_BOTH==2){
            Reportes.setSize(1920,1080);
        }else{
            Reportes.setSize(1090,620);
        }
        Reportes.setLocation(0,0);
        //Llamar a validate () o revalidate () es obligatorio cuando hace un remove () 
        PanelPrueba.removeAll();
        PanelPrueba.add(Reportes);
        //revalidate se llama en un contenedor una vez que se agregan nuevos componentes o se eliminan los antiguos.
        //esta llamada es una instrucción para indicar al administrador de diseño que debe restablecerse en función
        //de la nueva lista de componentes. revalidate activará una llamada para volver a pintar lo que el componente
        //cree que son "regiones sucias". Obviamente no todas las regiones en su JPanel son considerados sucios por el RepaintManager.
        PanelPrueba.revalidate();
        //le dice al administrador de diseño que vuelva a calcular el diseño (que es necesario al agregar componentes). 
        PanelPrueba.repaint();
    }//GEN-LAST:event_btnReporteMouseClicked

